export function getCompanyById(id: string): Company | undefined {
  return companies.find((c) => c.id === id);
}

export function getJobById(id: string): Job | undefined {
  return jobs.find((j) => j.id === id);
}

export function getInternshipById(id: string): Internship | undefined {
  return internships.find((i) => i.id === id);
}

export function getUserById(id: string): User | undefined {
  return users.find((u) => u.id === id);
}

function getUserSkillsForMatching() {
  return buildUserSkills(studentProfile.skills, skillAnalysis);
}

export function getJobsWithCompany() {
  const userSkills = getUserSkillsForMatching();
  return jobs
    .map((job) => ({
      ...job,
      company: getCompanyById(job.companyId)!,
      matchPercentage: computeMatchScore(userSkills, job.skills),
    }))
    .sort((a, b) => (b.matchPercentage ?? 0) - (a.matchPercentage ?? 0));
}

export function getInternshipsWithCompany() {
  const userSkills = getUserSkillsForMatching();
  return internships
    .map((int) => ({
      ...int,
      company: getCompanyById(int.companyId)!,
      matchPercentage: computeMatchScore(userSkills, int.requirements),
    }))
    .sort((a, b) => (b.matchPercentage ?? 0) - (a.matchPercentage ?? 0));
}

export function getApplicationsWithDetails() {
  return applications
    .filter((a) => a.studentId === currentUser.id)
    .map((app) => ({
      ...app,
      job: getJobById(app.jobId),
      company: getCompanyById(app.companyId),
    }));
}

